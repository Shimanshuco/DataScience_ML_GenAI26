# Accessing String

s = "Hello World"
# Accessing String using Indexing
print(s[0])  # H
print(s[1])  # e
print(s[2])  # l
print(s[3])  # l
print(s[4])  # o
print(s[5])  # Space
print(s[6])  # W
print(s[7])  # o
print(s[8])  # r
print(s[9])  # l
print(s[10])  # d

# Accessing String using Negative Indexing
print(s[-1])  # d
print(s[-2])  # l
print(s[-3])  # r

