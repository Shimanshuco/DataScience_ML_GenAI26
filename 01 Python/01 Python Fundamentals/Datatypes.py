#Datatypes in Python
# In Python, there are several built-in data types that are used to store and manipulate data.
# Some of the most common data types in Python include:
# 1. Numeric Types: These include integers (int), floating-point numbers (float), and complex numbers (complex).
# 2. Sequence Types: These include strings (str), lists (list), tuples (tuple), and ranges (range).
# 3. Mapping Types: These include dictionaries (dict).
# 4. Set Types: These include sets (set) and frozensets (frozenset).
# 5. Boolean Type: This includes the boolean values True and False (bool).
# 6. None Type: This includes the special value None, which represents the absence of a value (NoneType).
# In Python, you can use the type() function to check the data type of a variable or value.
# For example:

# int
a=10;
print("a=",a," ","type of a=",type(a));

#flot
b=3.14;
print("b=",b," ","type of b=",type(b));

#complex
c=2+3j;
print("c=",c," ","type of c=",type(c));
print("Real part of c=",c.real);
print("Imaginary part of c=",c.imag);

#str
d="Hello World";
print("d=",d," ","type of d=",type(d));

#List
e=[1,2,3,4,5];
print("e=",e," ","type of e=",type(e));

#Tuple
f=(1,2,3,4,5);
print("f=",f," ","type of f=",type(f));

#Dictionary
g={"name":"Himanshu Singh","age":25,"city":"New York"};
print("g=",g," ","type of g=",type(g));

#Set
h={1,2,3,4,5};
print("h=",h," ","type of h=",type(h));

#Boolean
i=True;
print("i=",i," ","type of i=",type(i));

#None
j=None;
print("j=",j," ","type of j=",type(j));


