# Keywords : Reserved words in Python that have special meanings and cannot be used as identifiers.
# Examples of keywords include: if, else, for, while, def, class, print, etc.
print("Hello, World!")  # 'print' is a keyword used to output text to the console.



# Identifiers : Names used to identify variables, functions, classes, etc. in Python. 
# They must follow certain rules and cannot be the same as keywords.
# Examples of identifiers include: my_variable, calculate_sum, Person, etc.
my_variable = 10  # 'my_variable' is an identifier that refers to the value 10. 

#Difference between variables and identifiers :
    # Variables are specific instances of data that can change during program execution, 
    # while identifiers are the names used to refer to those variables, functions, classes, etc. in the code.