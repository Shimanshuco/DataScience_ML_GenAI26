# Literals : Values that are directly written in the code, such as numbers, strings, and boolean values.
# Examples of literals in Python:
# Integer literal
x = 10  # This is an integer literal
print(x)  # Output: 10

# Float literal
y = 3.14  # This is a float literal
g = 2.5e-3  # This is a float literal in scientific notation, representing 0.0025
h = 1.5e+2  # This is a float literal in scientific notation, representing 150.0
print(y)  # Output: 3.14
print(g)  # Output: 0.0025
print(h)  # Output: 150.0

# String literal
name = "Alice"  # This is a string literal
print(name)  # Output: Alice

# Complex literal
z = 2 + 3j  # This is a complex literal representing the complex number with real part 2 and imaginary part 3
print(z)  # Output: (2+3j)

# Boolean literal
is_valid = True  # This is a boolean literal
print(is_valid)  # Output: True

# None literal
result = None  # This is a None literal
print(result)  # Output: None


# Binary literals
a = 0b1010  # This is a binary literal representing the decimal number 10
print(a)  # Output: 10

# Octal literals
b = 0o12  # This is an octal literal representing the decimal number 10
print(b)  # Output: 10

# Hexadecimal literals
c = 0xA  # This is a hexadecimal literal representing the decimal number 10
print(c)  # Output: 10


#String Literals
# String literals can be defined using single quotes, double quotes, or triple quotes.
# Single quotes
greeting = 'Hello, World!'  # This is a string literal using single quotes
print(greeting)  # Output: Hello, World!
# Double quotes
message = "Welcome to Python programming!"  # This is a string literal using double quotes
print(message)  # Output: Welcome to Python programming!
# Triple quotes
multiline_string = """This is a string literal
that spans multiple lines."""  # This is a string literal using triple quotes
print(multiline_string)

raw_string = r"C:\Users\Alice\Documents"  # This is a raw string literal, where backslashes are treated as literal characters
print(raw_string)  # Output: C:\Users\Alice\Documents

unicode = u"\U0001F600\U0001F606"  # This is a Unicode string literal representing the copyright symbol (©)
print(unicode)  # Output: ©




#Boolean
a = True + 5
print(a)  # Output: 6
b= False + 5
print(b)  # Output: 5
