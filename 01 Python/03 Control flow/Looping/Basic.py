# Looping : Loops are used to execute a block of code repeatedly until a certain condition is met.
# There are two main types of loops in Python: for loops and while loops.
# 1. For Loop : A for loop is used to iterate over a sequence (like a list, tuple, or string) or other iterable objects.
# Syntax:
# for variable in sequence:
#     # code block to be executed

# 2. While Loop : A while loop is used to execute a block of code as long as a specified condition is true.
# Syntax:
# while condition:
#     # code block to be executed