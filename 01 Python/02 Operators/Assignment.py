# Assignment Operators
# Assignment operators are used to assign values to variables.
# The assignment operators are:
# = (Simple Assignment)
# += (Add and Assign)
# -= (Subtract and Assign)
# *= (Multiply and Assign)
# /= (Divide and Assign)


x = 10  # Simple Assignment
print(x)  # Output: 10
x += 5  # Add and Assign
print(x)  # Output: 15
x -= 3  # Subtract and Assign
print(x)  # Output: 12
x *= 2  # Multiply and Assign
print(x)  # Output: 24
x /= 4  # Divide and Assign
print(x)  # Output: 6.0


# In python, a++ and a-- are not valid operators. Instead, you can use a += 1 and a -= 1 to achieve the same result.
