# Operators : Operators are special symbols that perform specific operations on operands (values or variables).
    #  They are used to manipulate data and perform calculations in programming languages.

# Types of Operators in Python:
    # 1. Arithmetic Operators: Used for mathematical operations.
    # 2. Comparison Operators: Used to compare values and return a boolean result.
    # 3. Logical Operators: Used to combine conditional statements.
    # 4. Assignment Operators: Used to assign values to variables.
    # 5. Bitwise Operators: Used to perform bit-level operations on integers.
    # 6. Membership Operators: Used to test if a value is in a sequence (like lists, tuples, etc.).
    # 7. Identity Operators: Used to compare the memory locations of two objects.