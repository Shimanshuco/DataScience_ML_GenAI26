# Membership Operators
# Membership operators are used to test if a value is in a sequence (like lists, tuples, etc.).
# The membership operators are:
# in (Returns True if a value is found in the sequence)
# not in (Returns True if a value is not found in the sequence)

my_list = [1, 2, 3, 4, 5]
print(3 in my_list)  # Output: True
print(6 in my_list)  # Output: False
print(3 not in my_list)  # Output: False
print(6 not in my_list)  # Output: True
my_tuple = ('a', 'b', 'c')
print('a' in my_tuple)  # Output: True
print('d' in my_tuple)  # Output: False
print('a' not in my_tuple)  # Output: False
