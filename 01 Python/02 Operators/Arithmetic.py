# Arithmetic Operators
# +, -, *, /, //, %, **
# Addition
a = 10
b = 5

print("a+b = ",a+b);

# Subtraction
print("a-b = ",a-b);

# Multiplication
print("a*b = ",a*b);

# Division
print("a/b = ",a/b);

# Floor Division 
print("a//b = ",a//b);

# Modulus
print("a%b = ",a%b);

# Exponentiation    
print("a**b = ",a**b);

